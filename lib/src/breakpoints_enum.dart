/// Breakpoint screen size definition for responsive design.
///
/// See the breakpoint system documentation for more information.
/// [https://material.io/design/layout/responsive-layout-grid.html#breakpoints].
enum BreakpointScreenSize {
  /// 0~599
  extraSmall,

  /// 600~904
  smallScaleBody,

  /// 905~1239
  smallFixBody,

  /// 1240~1439
  medium,

  /// 1440~
  large,
}
